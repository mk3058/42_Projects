#include "libft.h"

int	ft_isdigit(char *c)
{
	int	i;

	i = -1;
	printf("%s\n", c);
	while (c[++i])
	{
		if (c[i] < '0' || c[i] > '9')
			return (0);
	}
	return (1);
}
